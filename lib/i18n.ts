/**
 * Translation helper. Returns the translated string for the given key and language.
 * Falls back to English if the key doesn't have a translation for the requested language.
 * Falls back to the key itself if the key doesn't exist at all.
 */
export function t(key: string, lang: AppLanguage = "en"): string {
  const entry = translations[key];
  if (!entry) return key;
  return entry[lang] ?? entry.en ?? key;
}

/**
 * Creates a bound translator for a specific language.
 * Usage: const t = createTranslator("ar"); t("nav.dashboard") => "Ù„ÙˆØ­Ø© Ø§Ù„ØªØ­ÙƒÙ…"
 */
export function createTranslator(lang: AppLanguage) {
  return (key: string) => t(key, lang);
}




